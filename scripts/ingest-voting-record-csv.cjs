#!/usr/bin/env node
/**
 * ingest-voting-record-csv.cjs
 *
 * Injects a <!-- auto:voting-record start/end --> block into a politician
 * profile, populated from a curated CSV of their key votes.
 *
 * CSV schema expected (header row required):
 *   Date,Chamber,Bill_Number,Description,Vote,Result,Key_Vote,Category
 *
 * By default, only rows where Key_Vote = "Yes" are included. Pass
 * --include-all to include every row.
 *
 * The auto-block is inserted into the existing "### Voting Record" or
 * "## Voting Record" section. If neither exists, a "## Voting Record"
 * section is created just before Sources. Idempotent: re-running with
 * a different CSV overwrites the block without touching surrounding
 * editorial content.
 *
 * Usage:
 *   node scripts/ingest-voting-record-csv.cjs \
 *     --profile "Marco Rubio" \
 *     --csv data/bulk/voting-records/marco_rubio_comprehensive_legislative_record.csv
 *   node scripts/ingest-voting-record-csv.cjs --profile "..." --csv ... --write
 */
const fs = require("fs")
const path = require("path")

const ROOT = path.join(__dirname, "..")
const argv = process.argv.slice(2)
const WRITE = argv.includes("--write")
const INCLUDE_ALL = argv.includes("--include-all")

function getArg(name) {
  const i = argv.indexOf(name)
  return i === -1 ? null : argv[i + 1]
}

const PROFILE = getArg("--profile")
const CSV = getArg("--csv")

if (!PROFILE || !CSV) {
  console.error("Usage: --profile <title> --csv <path> [--write] [--include-all]")
  process.exit(1)
}

const BLOCK_START = "<!-- auto:voting-record start -->"
const BLOCK_END = "<!-- auto:voting-record end -->"

// ─── CSV parsing ────────────────────────────────────────────────────
function parseCsvLine(line) {
  const out = []
  let cur = ""
  let inQuote = false
  for (let i = 0; i < line.length; i++) {
    const c = line[i]
    if (inQuote) {
      if (c === '"' && line[i + 1] === '"') { cur += '"'; i++ }
      else if (c === '"') inQuote = false
      else cur += c
    } else {
      if (c === ',') { out.push(cur); cur = "" }
      else if (c === '"') inQuote = true
      else cur += c
    }
  }
  out.push(cur)
  return out
}

function readCsv(filePath) {
  const raw = fs.readFileSync(filePath, "utf-8")
  // Split on newlines but respect lines with embedded commas/quotes
  const lines = raw.split(/\r?\n/).filter((l) => l.length > 0)
  const header = parseCsvLine(lines[0]).map((h) => h.trim())
  const rows = []
  for (let i = 1; i < lines.length; i++) {
    const cols = parseCsvLine(lines[i])
    if (cols.every((c) => !c)) continue
    const row = {}
    header.forEach((h, idx) => (row[h] = (cols[idx] || "").trim()))
    rows.push(row)
  }
  return rows
}

// ─── Profile location ────────────────────────────────────────────────
function findProfilePath(title) {
  const targets = []
  function walk(dir) {
    for (const name of fs.readdirSync(dir)) {
      const full = path.join(dir, name)
      let stat
      try { stat = fs.statSync(full) } catch { continue }
      if (stat.isDirectory()) walk(full)
      else if (name.endsWith(".md")) targets.push(full)
    }
  }
  walk(path.join(ROOT, "content", "Politicians"))
  for (const f of targets) {
    const text = fs.readFileSync(f, "utf-8")
    const m = text.match(/^---\n([\s\S]*?)\n---/)
    if (!m) continue
    const titleMatch = m[1].match(/^title:\s*(.+)$/m)
    if (!titleMatch) continue
    if (titleMatch[1].trim() === title) return f
  }
  return null
}

// ─── Block rendering ─────────────────────────────────────────────────
function escPipe(s) { return String(s || "").replace(/\|/g, "\\|") }

function renderBlock(rows, profile) {
  const lines = []
  lines.push(BLOCK_START)
  lines.push(`<!-- Generated by scripts/ingest-voting-record-csv.cjs - do not edit by hand -->`)
  lines.push("")

  const included = INCLUDE_ALL ? rows : rows.filter((r) => /^(yes|y|true|1)$/i.test(r.Key_Vote || ""))
  const excluded = rows.length - included.length

  if (included.length === 0) {
    lines.push(`_No key votes found in CSV (${rows.length} total rows). Use --include-all to show every row._`)
    lines.push(BLOCK_END)
    return lines.join("\n")
  }

  lines.push(`**${included.length} key votes** across ${profile}'s career${excluded > 0 ? ` (plus ${excluded} routine votes excluded — pass --include-all to show everything)` : ""}.`)
  lines.push("")
  lines.push("| Date | Bill | Issue | Vote | Outcome | Category |")
  lines.push("|---|---|---|---|---|---|")

  // Sort newest first
  const sorted = [...included].sort((a, b) => (b.Date || "").localeCompare(a.Date || ""))
  for (const r of sorted) {
    lines.push(
      `| ${escPipe(r.Date)} | ${escPipe(r.Bill_Number)} | ${escPipe(r.Description)} | **${escPipe(r.Vote)}** | ${escPipe(r.Result)} | ${escPipe(r.Category)} |`,
    )
  }
  lines.push("")
  lines.push(`_Source CSV: ${path.relative(ROOT, CSV).replace(/\\/g, "/")}. Key votes curated for signature-importance. Regenerate: \`node scripts/ingest-voting-record-csv.cjs --profile "${profile}" --csv <path> --write\`._`)
  lines.push(BLOCK_END)
  return lines.join("\n")
}

// ─── Block insertion ─────────────────────────────────────────────────
function applyBlock(content, block) {
  // Always promote any existing H3 "Voting Record"/"Key Votes" to H2 first —
  // an H3 landing means a past run inserted under Sources or Influence
  // Network, which routes to the wrong tab (analysis) in ProfileTabs.
  // Top-level H2 is required for the Key Votes tab to pick it up.
  const h3 = content.match(/^###\s+(Voting Record|Key Votes)\s*$/m)
  if (h3) {
    content = content.replace(h3[0], h3[0].replace(/^###/, "##"))
  }

  const startIdx = content.indexOf(BLOCK_START)
  const endIdx = content.indexOf(BLOCK_END)
  if (startIdx !== -1 && endIdx !== -1 && endIdx > startIdx) {
    return content.slice(0, startIdx) + block + content.slice(endIdx + BLOCK_END.length)
  }

  // Prefer H2 "## Voting Record" or "## Key Votes" — these are top-level
  // sections the ProfileTabs wrapper buckets into the Key Votes tab. An
  // existing H3 "### Voting Record" (legacy placement under Sources or
  // Influence Network) is PROMOTED to H2 so the content actually lands
  // in the correct tab.
  const h2Match = content.match(/^##\s+(Voting Record|Key Votes)\s*$/m)
  if (h2Match) {
    const headingEnd = content.indexOf(h2Match[0]) + h2Match[0].length
    return content.slice(0, headingEnd) + "\n\n" + block + "\n" + content.slice(headingEnd)
  }
  // Promote any existing H3 "### Voting Record" to H2 + insert block
  const h3Match = content.match(/^###\s+(Voting Record|Key Votes)\s*$/m)
  if (h3Match) {
    const promoted = h3Match[0].replace(/^###/, "##")
    const withPromoted = content.replace(h3Match[0], promoted)
    const headingEnd = withPromoted.indexOf(promoted) + promoted.length
    return withPromoted.slice(0, headingEnd) + "\n\n" + block + "\n" + withPromoted.slice(headingEnd)
  }

  // No heading — insert a new "## Voting Record" section just before "## Sources"
  const sourcesMatch = content.match(/^##\s+Sources\s*$/m)
  if (sourcesMatch) {
    const idx = content.indexOf(sourcesMatch[0])
    return content.slice(0, idx) + `## Voting Record\n\n${block}\n\n---\n\n` + content.slice(idx)
  }

  // Last resort: append to end
  return content.trimEnd() + `\n\n## Voting Record\n\n${block}\n`
}

// ─── Main ────────────────────────────────────────────────────────────
function main() {
  const absCsv = path.resolve(CSV)
  if (!fs.existsSync(absCsv)) {
    console.error(`CSV not found: ${absCsv}`)
    process.exit(2)
  }
  const profilePath = findProfilePath(PROFILE)
  if (!profilePath) {
    console.error(`No profile found with title "${PROFILE}" under content/Politicians/`)
    process.exit(3)
  }

  const rows = readCsv(absCsv)
  console.log(`Read ${rows.length} rows from ${path.relative(ROOT, absCsv)}`)
  console.log(`Profile: ${path.relative(ROOT, profilePath)}`)

  const block = renderBlock(rows, PROFILE)
  const before = fs.readFileSync(profilePath, "utf-8")
  const after = applyBlock(before, block)

  if (after === before) {
    console.log("No change (block already matches).")
    return
  }

  if (!WRITE) {
    console.log("")
    console.log("--- dry run. Pass --write to apply. Preview: ---")
    console.log(block.split("\n").slice(0, 15).join("\n"))
    console.log("...")
    return
  }

  fs.writeFileSync(profilePath, after, "utf-8")
  console.log(`✓ Wrote voting record block to ${path.relative(ROOT, profilePath)}`)
}

main()
